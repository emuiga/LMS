    //method to insert values to users table
    public void insertSignupDetails() {
        String name = var_username.getText();
        String email = var_mail.getText();
        String password = var_password.getText();
        String contact = var_telephone.getText();
        
        try {
            Connection con = DBConnection.getConnection();
            String sql = "insert into users(name,email,password,contact) values (?,?,?,?)";
            PreparedStatement prepst = con.prepareStatement(sql);
            
            prepst.setString(1, name);
            prepst.setString(2, email);
            prepst.setString(3, password);
            prepst.setString(4, contact);
            
            int updatedRowCount = prepst.executeUpdate();
            
            if (updatedRowCount > 0){
                JOptionPane.showMessageDialog(this, "SignUp Successful");
                LoginPage page = new LoginPage();
                page.setVisible(true);
                dispose(); 
            }else{
                JOptionPane.showMessageDialog(this, "SignUp Failed");
            }

        }catch (Exception e) {
    e.printStackTrace();
        }
    }
    
    //validation
    public boolean validateSignup(){
        String name = var_username.getText();
        String email = var_mail.getText();
        String password = var_password.getText();
        String contact = var_telephone.getText();
        String phoneNumber = var_telephone.getText();
        int length = phoneNumber.length();
        
        if (name.equals("")){
            JOptionPane.showMessageDialog(this, "please enter username");
            return false;
        }
        if (email.equals("") || !email.matches("^.+@.+\\..+$")){
            JOptionPane.showMessageDialog(this, "please enter valid email");
            return false;
        }
        if (password.equals("")){
            JOptionPane.showMessageDialog(this, "please enter password");
            return false;
        }
        if (contact.equals("") || phoneNumber.length()<10){
             JOptionPane.showMessageDialog(this, "please enter phone number");
            return false;   
            }
        return true;
    }
    
