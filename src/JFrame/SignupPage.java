    public boolean restrictDuplicates(){
        String name = var_username.getText();
        boolean isExist = false;
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/nca_lmsdb","root","");
            
            PreparedStatement prepst = con.prepareStatement("select * from users where name = ?");
            prepst.setString(1, name);
            ResultSet rs = prepst.executeQuery();
            if (rs.next()) {
                isExist =  true;
            }else {
                isExist = false;
            }
        } catch (Exception e){
            e.printStackTrace();
        }
        return isExist;
    }
    
